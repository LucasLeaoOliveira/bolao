public class Apostadores {
    private String nome;
    private Float saldo;
    private String cpf;
    private int pontuacaoTotal;
    private int golApostaMand;
    private int golApostaVist;
    
    
    
    
    
    
    
    
    /*GETTERS AND SETTERS
    ==================*/


    
    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Float getSaldo() {
        return this.saldo;
    }

    public void setSaldo(Float saldo) {
        this.saldo = saldo;
    }

    public String getCpf() {
        return this.cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public int getPontuacaoTotal() {
        return this.pontuacaoTotal;
    }

    public void setPontuacaoTotal(int pontuacaoTotal) {
        this.pontuacaoTotal = pontuacaoTotal;
    }
        

    public int getGolApostaMand() {
        return this.golApostaMand;
    }

    public void setGolApostaMand(int golApostaMand) {
        this.golApostaMand = golApostaMand;
    }

    public int getGolApostaVist() {
        return this.golApostaVist;
    }

    public void setGolApostaVist(int golApostaVist) {
        this.golApostaVist = golApostaVist;
    }

    }



       
    
